/*
Сбор черники

В фермерском хозяйстве в Карелии выращивают чернику. Она растет на круглой грядке, причем кусты высажены только по окружности.
Таким образом, у каждого куста есть ровно два соседних. Всего на грядке растет N кустов.

Эти кусты обладают разной урожайностью, поэтому ко времени сбора на них выросло различное число ягод – на i-ом кусте выросло ai ягод.

В этом фермерском хозяйстве внедрена система автоматического сбора черники.
Эта система состоит из управляющего модуля и нескольких собирающих модулей.
Собирающий модуль за один заход, находясь непосредственно перед некоторым кустом, собирает ягоды с этого куста и с двух соседних с ним.

Напишите программу для нахождения максимального числа ягод, которое может собрать за один заход собирающий модуль,
находясь перед некоторым кустом заданной во входном файле грядки.

Входные данные
Первая строка входного файла INPUT.TXT содержит целое число N (3 ≤ N ≤ 1000) – количество кустов черники.
Вторая строка содержит N целых положительных чисел a1, a2, ..., aN – число ягод черники, растущее на соответствующем кусте.
Все ai не превосходят 1000.

Выходные данные
В выходной файл OUTPUT.TXT выведите ответ на задачу.
*/

Console.Clear();

string path_input = Directory.GetCurrentDirectory() + "\\INPUT.txt";
string path_output = Directory.GetCurrentDirectory() + "\\OUTPUT.txt";

string[] lines = System.IO.File.ReadAllLines(path_input);

int n = int.Parse(lines[0]);
int[] arr = lines[1].Split(' ').Select(i => int.Parse(i)).ToArray();

int rezult = 0;

for (int i = 1; i <= arr.Length - 2; i++) {
    if (rezult < arr[i - 1] + arr[i] + arr[i + 1])
        rezult = arr[i - 1] + arr[i] + arr[i + 1];
}

if (rezult < arr[n - 1] + arr[0] + arr[1])
    rezult = arr[n - 1] + arr[0] + arr[1];

if (rezult < arr[n - 2] + arr[n - 1] + arr[0])
    rezult = arr[n - 2] + arr[n - 1] + arr[0];

using (StreamWriter writer = new StreamWriter(path_output, false))
    await writer.WriteAsync(rezult.ToString());